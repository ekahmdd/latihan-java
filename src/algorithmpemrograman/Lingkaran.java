package algorithmpemrograman;

public class Lingkaran {
    public static void main(String[] args) {
    double r = 8;
    final double pi = 3.14;
    double L = pi * (r*r);
    double K = 2 * pi * r;
    
    System.out.println("Luas Linkaran Adalah: " + L);
    System.out.println("Keliling Linkaran Adalah: " + K);
    }
}
